    <script>
      // initialize the validator function
      validator.message.date = 'not a real date';

      // validate a field on "blur" event, a 'select' on 'change' event & a '.reuired' classed multifield on 'keyup':
      $('form')
        .on('blur', 'input[required], input.optional, select.required, div.required', validator.checkField)
        .on('change', 'select.required', validator.checkField)
        .on('keypress', 'input[required][pattern]', validator.keypress);

      $('.multi.required').on('keyup blur', 'input', function() {
        validator.checkField.apply($(this).siblings().last()[0]);
      });

      $('form').submit(function(e) {
        e.preventDefault();
        var submit = true;
        ed = document.getElementById("editor");
        d = document.getElementById("Descripcion");
        if (submit && ed.innerText.length <= -1)
        {
          alert('El Campo Descripcion debe contener mas de 0 caracteres');  
          submit = false;
        }else{
          d.innerText = ed.innerHTML;
        }
        

        // evaluate the form using generic validaing
        if (!validator.checkAll($(this))) {
          submit = false;
        }
        


        if (submit)
          this.submit();

        return false;
      });
    </script>
